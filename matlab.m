syms a b c u v w x y x1 x2 x3 y1 y2 y3 d
S=solve(a == y1-y2,...
    b == x2-x1,...
    c == x1*y2 - x2*y1,...
    u == y2 - y3,...
    v == x3 - x2,...
    w == x2*y3 - x3*y2,...
    (x-x2)^2+(y-y2)^2==d^2,...
    (a*x+b*y+c)/sqrt(a^2+b^2)==(u*x+v*y+w)/sqrt(u^2+v^2))
x_eq = simplify(subs(S.x,[a b c u v w], [y1-y2 x2-x1 x1*y2-x2*y1 y2-y3 x3-x2 x2*y3-x3*y2]))
y_eq = simplify(subs(S.y,[a b c u v w], [y1-y2 x2-x1 x1*y2-x2*y1 y2-y3 x3-x2 x2*y3-x3*y2]))